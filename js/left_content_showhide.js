function showhide(){
	document.all.left_content.style.display=(document.all.left_content.style.display =='none')?'':'none'
	document.all.right_arrow.style.display=(document.all.right_arrow.style.display =='none')?'':'none'
	document.all.left_arrow.style.display=(document.all.left_arrow.style.display =='none')?'':'none'
}